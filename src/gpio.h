#ifndef GPIO_H_
#define GPIO_H_

/*--------------------------------------------------------------------------------
                                   INCLUDES
--------------------------------------------------------------------------------*/

/*--------------------------------------------------------------------------------
                                LITERAL CONSTANTS
--------------------------------------------------------------------------------*/

/*--------------------------------------------------------------------------------
                                     MACROS
--------------------------------------------------------------------------------*/

/*--------------------------------------------------------------------------------
                                      TYPES
--------------------------------------------------------------------------------*/

typedef struct
{
    GPIO_TypeDef  * port;
    uint32_t        pin;
} gpio_type;

typedef enum
{
    GPIO_STATE_LOW,
    GPIO_STATE_HIGH
} gpio_state_type;


/*--------------------------------------------------------------------------------
                                GLOBAL VARIABLES
--------------------------------------------------------------------------------*/

/*--------------------------------------------------------------------------------
                                   PROCEDURES
--------------------------------------------------------------------------------*/

void gpio_cfg_input
    (
    const gpio_type   * gpio
    );

void gpio_cfg_output
    (
    const gpio_type   * gpio
    );

gpio_state_type gpio_input_read
    (
    const gpio_type   * gpio
    );

void gpio_output_set
    (
    const gpio_type   * gpio,
    gpio_state_type     state
    );


#endif /* GPIO_H_ */
